package aptiprogramme.OtherAptiProgramme;

public class ProMultiflication {
	public static void main(String[] args) {
		int a=3;
		int b=4;
		int sum3=0;
		int sum4=0;
		
		for (int i =0; i <b; i++) {
			sum3+=a;
		}
		for (int i =0; i <a; i++) {
			sum4+=b;
		}
		System.out.println("3+3+3+3 = "+sum3);
		System.out.println("4+4+4 = "+sum4);
	}

}
